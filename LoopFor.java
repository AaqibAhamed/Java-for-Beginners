class LoopFor
{
	
	public static void main(String[]arg)	
	{
	  
	  int b=10;int c=1;
	  
	  for(int x=1;x<10;x++)

	  {
	    System.out.println("Numbers of the loop = "+ x);	
		
	  }	

	  
	  System.out.println();
	  System.out.println("***** Second for loop *****");
	  System.out.println();


	  for(int a=1;a<b;a++,b--)

	  {
	    System.out.println("Numbers of the loop :- a = "+a+" b = "+b);
	    	
		
	  }

	  System.out.println();
	  System.out.println("***** third for loop *****");
	  System.out.println();	

	  for(;c<5;c++)

	  {
	    System.out.println("Numbers of the loop = "+ c);	
		
	  }

	}




}